# diff variables example
x = 'Hello Python'
print("Data type: ", type(x))

x = range(6)
print("Data type: ", type(x))

x = 1657463874
print("Data type: ", type(x))

x = 1j
print("Data type: ", type(x))

x = 65465465454.1584654564687465
print("Data type: ", type(x))

x = 200000.00013246587897
print("Data type: ", type(x))

x = True
print("Data type: ", type(x))

x = [1, 2, 3, ]
print("Data type: ", type(x))

x = {"abc"}
print("Data type: ", type(x))

x = frozenset({"apple", "banana", "cherry"})
print("Data type: ", type(x))

x = {"abc": 1}
print("Data type: ", type(x))

x = (1, 2, 3)
print("Data type: ", type(x))

x = None
print("Data type: ", type(x))

x = b"Hello"
print("Data type: ", type(x))

x = bytearray(5)
print("Data type: ", type(x))

x = memoryview(bytearray(5))
print("Data type: ", type(x))
